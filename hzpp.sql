--KREIRANJE tablica
CREATE TABLE info (
    info_id    NUMBER NOT NULL,
    polaziste  NUMBER NOT NULL,
    odrediste  NUMBER NOT NULL,
    cijena     NUMBER NOT NULL,
    trajanje   VARCHAR2(15) NOT NULL,
    vlak_id    NUMBER NOT NULL
);

ALTER TABLE info ADD CONSTRAINT info_pk PRIMARY KEY ( info_id );

CREATE TABLE karta (
    karta_id         NUMBER NOT NULL,
    info_id          NUMBER NOT NULL,
    datum            VARCHAR2(15) NOT NULL,
    kupac_id         NUMBER NOT NULL,
    vrijeme_polaska  VARCHAR2(15) NOT NULL
);

ALTER TABLE karta ADD CONSTRAINT karta_pk PRIMARY KEY ( karta_id );

CREATE TABLE kupac (
    kupac_id   NUMBER NOT NULL,
    ime        VARCHAR2(20) NOT NULL,
    prezime    VARCHAR2(20) NOT NULL,
    adresa     VARCHAR2(60) NOT NULL,
    tel        VARCHAR2(12) NOT NULL,
    datum      VARCHAR2(15) NOT NULL,
    popust_id  VARCHAR2(20) NOT NULL
);

ALTER TABLE kupac ADD CONSTRAINT kupac_pk PRIMARY KEY ( kupac_id );

CREATE TABLE popust (
    popust_id  VARCHAR2(20) NOT NULL,
    postotak   NUMBER NOT NULL
);

ALTER TABLE popust ADD CONSTRAINT popust_pk PRIMARY KEY ( popust_id );

CREATE TABLE postaja (
    postaja_id  NUMBER NOT NULL,
    naselje     VARCHAR2(60) NOT NULL
);

ALTER TABLE postaja ADD CONSTRAINT postaja_pk PRIMARY KEY ( postaja_id );

CREATE TABLE uloge (
    uloge_id    NUMBER NOT NULL,
    postaja_id  NUMBER,
    vlak_id     NUMBER,
    zap_id      NUMBER NOT NULL
);

ALTER TABLE uloge ADD CONSTRAINT uloge_pk PRIMARY KEY ( zap_id );

CREATE TABLE vlak (
    vlak_id   NUMBER NOT NULL,
    brzi      CHAR(1) NOT NULL,
    putnicki  CHAR(1) NOT NULL
);

ALTER TABLE vlak ADD CONSTRAINT vlak_pk PRIMARY KEY ( vlak_id );

CREATE TABLE zaposlenik (
    zap_id   NUMBER NOT NULL,
    ime      VARCHAR2(20) NOT NULL,
    prezime  VARCHAR2(20) NOT NULL,
    adresa   VARCHAR2(40) NOT NULL,
